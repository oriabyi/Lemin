#include "libft.h"

int			are_nums(char *s)
{
	if (s == NULL)
		return (-1);
	else
	{
		while (*s)
		{
			if (!(*s >= '0' && *s <= '9'))
				return (0);
			s++;
		}
	}
	return (1);
}
